/**
 * 已经完成的工作
 */
package com.audit.entity.work;

import java.io.Serializable;

import com.audit.entity.Common;

/**
 * @author DengXin
 */
public class MyCompleteWorkInfo extends Common implements Serializable{

	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 686153541770711953L;

	/**
	 * 流程ID
	 */
	private String id;
	
	/**
	 * 业务数据ID
	 */
	private String projectId;
	
	/**
	 * 处理模块ID
	 */
	private String muduleId;
	
	/**
	 * 处理模块名称
	 */
	private String moduleName;

	/**
	 * 处理模块URL
	 */
	private String mouduleUrl;
	
	/**
	 * 完成之前的状态
	 */
	private String beforeState;
	
	/**
	 * 完成之后的状态
	 */
	private String afterState;
	
	/**
	 * 业务状态ID
	 */
	private String stateId;
	
	/**
	 * 当前状态
	 */
	private String nowState;
	
	/**
	 * 操作方法
	 */
	private String function;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the projectId
	 */
	public String getProjectId() {
		return projectId;
	}

	/**
	 * @param projectId the projectId to set
	 */
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	/**
	 * @return the muduleId
	 */
	public String getMuduleId() {
		return muduleId;
	}

	/**
	 * @param muduleId the muduleId to set
	 */
	public void setMuduleId(String muduleId) {
		this.muduleId = muduleId;
	}

	/**
	 * @return the moduleName
	 */
	public String getModuleName() {
		return moduleName;
	}

	/**
	 * @param moduleName the moduleName to set
	 */
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	/**
	 * @return the mouduleUrl
	 */
	public String getMouduleUrl() {
		return mouduleUrl;
	}

	/**
	 * @param mouduleUrl the mouduleUrl to set
	 */
	public void setMouduleUrl(String mouduleUrl) {
		this.mouduleUrl = mouduleUrl;
	}

	/**
	 * @return the beforeState
	 */
	public String getBeforeState() {
		return beforeState;
	}

	/**
	 * @param beforeState the beforeState to set
	 */
	public void setBeforeState(String beforeState) {
		this.beforeState = beforeState;
	}

	/**
	 * @return the afterState
	 */
	public String getAfterState() {
		return afterState;
	}

	/**
	 * @param afterState the afterState to set
	 */
	public void setAfterState(String afterState) {
		this.afterState = afterState;
	}

	/**
	 * @return the stateId
	 */
	public String getStateId() {
		return stateId;
	}

	/**
	 * @param stateId the stateId to set
	 */
	public void setStateId(String stateId) {
		this.stateId = stateId;
	}

	/**
	 * @return the nowState
	 */
	public String getNowState() {
		return nowState;
	}

	/**
	 * @param nowState the nowState to set
	 */
	public void setNowState(String nowState) {
		this.nowState = nowState;
	}

	/**
	 * @return the function
	 */
	public String getFunction() {
		return function;
	}

	/**
	 * @param function the function to set
	 */
	public void setFunction(String function) {
		this.function = function;
	}
}
