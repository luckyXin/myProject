$.extend($.fn.validatebox.defaults.rules, {
	maxLength : {
		validator : function(value, param) {
			return value.length <= param[0];
		},
		message : '请输入小于 {0} 的值.'
	}

});

$.extend($.fn.validatebox.defaults.rules, {
	minLength : {
		validator : function(value, param) {
			return value.length >= param[0];
		},
		message : '请输入大于 {0} 的值.'
	}
});


