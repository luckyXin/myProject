package com.audit.entity.project;

import java.io.Serializable;

/**
 * @author User
 * 
 */
public class AuditContent implements Serializable {

	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 3330849597058127428L;

	/**
	 * 审批ID
	 */
	private String id;

	/**
	 * 审批项目
	 */
	private String projectDateId;

	/**
	 * 审批意见
	 */
	private String remark;

	/**
	 * 审批用户帐号
	 */
	private String userAccount;

	/**
	 * 是否通过 0 未通过 1 通过
	 */
	private String passState;

	/**
	 * 是否提交
	 */
	private String isSubmit;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the projectDateId
	 */
	public String getProjectDateId() {
		return projectDateId;
	}

	/**
	 * @param projectDateId the projectDateId to set
	 */
	public void setProjectDateId(String projectDateId) {
		this.projectDateId = projectDateId;
	}

	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}

	/**
	 * @return the userAccount
	 */
	public String getUserAccount() {
		return userAccount;
	}

	/**
	 * @param userAccount the userAccount to set
	 */
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}

	/**
	 * @return the passState
	 */
	public String getPassState() {
		return passState;
	}

	/**
	 * @param passState the passState to set
	 */
	public void setPassState(String passState) {
		this.passState = passState;
	}

	/**
	 * @return the isSubmit
	 */
	public String getIsSubmit() {
		return isSubmit;
	}

	/**
	 * @param isSubmit the isSubmit to set
	 */
	public void setIsSubmit(String isSubmit) {
		this.isSubmit = isSubmit;
	}

}
