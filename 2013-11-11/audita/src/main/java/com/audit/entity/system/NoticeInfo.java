package com.audit.entity.system;

import java.io.Serializable;
import java.util.Date;

import com.audit.entity.Common;

/**
 * @author dengyong 公告
 *
 */
public class NoticeInfo extends Common implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private String id;           //id
	
	private String title;        //标题
	
	private String content;      //公告信息
	
	private Date createtime;    //发布时间
	
	private String fabutime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Date getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}

	public String getFabutime() {
		return fabutime;
	}

	public void setFabutime(String fabutime) {
		this.fabutime = fabutime;
	}
   
	
	
	
	
}
