package com.audit.entity.staff;



import java.io.Serializable;

import com.audit.entity.Common;

/**
 * 施工企业实体对象
 * @author dengyong
 *
 */
public class Construction extends Common implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private String id;        //施工企业id
	
	private String constructname;  //企业名称
	
	private String deptreferred;   //企业简称
    
	private String enterprisetype;  //企业类型
	
	private String deptcode;        //组织机构代码
	
	private String regaddress;      //企业注册地址
	
	private Float  regfunds;         //注册资金
	
	private String legal;           //法人代表
	
	private String deptqualifica;  //企业资质
	
	private String email;       //电子邮箱
	
	private String address;          //企业地址
	
	private Integer ischose;         //是否区比选库企业1表示是,0表示否
	
	private Integer state;           //状态0表示启用1表示表示禁用2表示删除
	
	private String remark;            //备注

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getConstructname() {
		return constructname;
	}

	public void setConstructname(String constructname) {
		this.constructname = constructname;
	}

	public String getDeptreferred() {
		return deptreferred;
	}

	public void setDeptreferred(String deptreferred) {
		this.deptreferred = deptreferred;
	}

	public String getEnterprisetype() {
		return enterprisetype;
	}

	public void setEnterprisetype(String enterprisetype) {
		this.enterprisetype = enterprisetype;
	}

	public String getDeptcode() {
		return deptcode;
	}

	public void setDeptcode(String deptcode) {
		this.deptcode = deptcode;
	}

	public String getRegaddress() {
		return regaddress;
	}

	public void setRegaddress(String regaddress) {
		this.regaddress = regaddress;
	}

	public Float getRegfunds() {
		return regfunds;
	}

	public void setRegfunds(Float regfunds) {
		this.regfunds = regfunds;
	}

	public String getLegal() {
		return legal;
	}

	public void setLegal(String legal) {
		this.legal = legal;
	}

	

	public String getDeptqualifica() {
		return deptqualifica;
	}

	public void setDeptqualifica(String deptqualifica) {
		this.deptqualifica = deptqualifica;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Integer getIschose() {
		return ischose;
	}

	public void setIschose(Integer ischose) {
		this.ischose = ischose;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
}
