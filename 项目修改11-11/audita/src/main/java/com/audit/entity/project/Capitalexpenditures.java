package com.audit.entity.project;

import java.io.Serializable;

/**
 * @author xwx
 *
 */
public class Capitalexpenditures implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String id;//主键 资金支出id

	private String unit;//单位类型
	
	private String contractamount; //合同金额
	
	private String capitalexpendituresUnit;//资金支出单位name
	
	private String stopAuditTime;//截止审计日
	
	private String amountpaid;//已付金额
	
	private String mainAuditInfoid;//yes null 外键id
	
	private String ttvmoney;//主审审核金额

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getContractamount() {
		return contractamount;
	}

	public void setContractamount(String contractamount) {
		this.contractamount = contractamount;
	}

	public String getCapitalexpendituresUnit() {
		return capitalexpendituresUnit;
	}

	public void setCapitalexpendituresUnit(String capitalexpendituresUnit) {
		this.capitalexpendituresUnit = capitalexpendituresUnit;
	}

	public String getStopAuditTime() {
		return stopAuditTime;
	}

	public void setStopAuditTime(String stopAuditTime) {
		this.stopAuditTime = stopAuditTime;
	}

	public String getAmountpaid() {
		return amountpaid;
	}

	public void setAmountpaid(String amountpaid) {
		this.amountpaid = amountpaid;
	}

	public String getMainAuditInfoid() {
		return mainAuditInfoid;
	}

	public void setMainAuditInfoid(String mainAuditInfoid) {
		this.mainAuditInfoid = mainAuditInfoid;
	}

	public String getTtvmoney() {
		return ttvmoney;
	}

	public void setTtvmoney(String ttvmoney) {
		this.ttvmoney = ttvmoney;
	}
	
  
}
