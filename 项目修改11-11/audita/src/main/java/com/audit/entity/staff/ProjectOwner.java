package com.audit.entity.staff;

import java.io.Serializable;

import com.audit.entity.Common;

/**
 * 项目业主管理
 * @author dengyong
 *
 */
public class ProjectOwner extends Common implements Serializable{

	private static final long serialVersionUID = 1L;
	
	/**
	 * id
	 */
	private String id;
	
	/**
	 * 业主名称
	 */
	private String ownerName;
	
	/**
	 * 业主简称
	 */
	private String ownerReferred;
	
	/**
	 * 单位类型
	 */
	private  String  unitTypeId;  
	
	/**
	 * 单位类型
	 */
	private  String  unitType;  
	
	/**
	 * 是否政府投资项目建设主要单位  1表示是 0表示否
	 */
	private  Integer isMainUnit;
	
	/**
	 * 状态   0表示禁用  1表示启用   2表示删除
	 */
	private Integer state;           
	
	/**
	 * 备注
	 */
	private String remark;
	
	public String getUnitTypeId() {
		return unitTypeId;
	}

	public void setUnitTypeId(String unitTypeId) {
		this.unitTypeId = unitTypeId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getOwnerName() {
		return ownerName;
	}

	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	public String getOwnerReferred() {
		return ownerReferred;
	}

	public void setOwnerReferred(String ownerReferred) {
		this.ownerReferred = ownerReferred;
	}

	public String getUnitType() {
		return unitType;
	}

	public void setUnitType(String unitType) {
		this.unitType = unitType;
	}

	public Integer getIsMainUnit() {
		return isMainUnit;
	}

	public void setIsMainUnit(Integer isMainUnit) {
		this.isMainUnit = isMainUnit;
	}
}
